					<h2 class="title">ACM大赛介绍</h2>
					<p class="byline"></p>
					<div class="entry">
						<blockquote>
						<p>　　ACM/ICPC(ACM International Collegiate Programming Contest) ACM国际大学生程序设计竞赛，由美国计算机协会ACM（Association for Computing Machinery）主办，首次举办于1970年，当时在美国德克萨斯A&amp;M大学举办了首届比赛。1977年，在ACM计算机科学会议期间举办了首次总决赛，作为一种全新的发现和培养计算机科学顶尖学生的方式，竞赛很快得到美国和加拿大各大学的积极响应，是世界上公认的规模最大、水平最高的大学生程序设计竞赛，并被尊称为程序设计竞赛领域的奥林克大赛，在国际上享有很高的声誉，也是企业挑选计算机专业学生的重要参考依据。参赛者被称为ACMer。目前比赛演变成为一年一届的多国参与的国际性比赛。迄今已经举办了35届。<br />
　　最初几届比赛的参赛队伍主要来自美国和加拿大，后来逐渐发展成为一项世界范围内的竞赛。特别是自1997年IBM开始赞助赛事之后，赛事规模增长迅速。1997年，总共有来自560所大学的840支队伍参加比赛。而到了2004年，这一数字迅速增加到840所大学的4109支队伍并以每年10-20%的速度在增长。1980年代，ACM将竞赛的总部设在位于美国德克萨斯州的贝勒大学。<br />
　　在赛事的早期，冠军多为美国和加拿大的大学获得。而进入1990年代后期以来， 俄罗斯和其它一些东欧国家的大学连夺数次冠军。来自中国大陆的上海交通大学代表队则在2002年美国夏威夷第26届、2005年上海举行的第29届全球总决赛、2010第34届全球总决赛上三次夺得总冠军，而浙江大学在2011年第35届全球总决赛（美国佛罗里达州）中夺冠。由此，赛事的竞争格局已经由最初的北美大学一枝独秀演变成目前的亚欧对抗的局面。</p>
						</blockquote>
