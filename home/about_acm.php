					<h2 class="title">ACM大赛规则</h2>
					<p class="byline"></p>
					<div class="entry">
						<blockquote>
<p>　　ACM/ICPC以团队的形式代表各学校参赛，每队由3名队员组成。每位队员必须是入校5年内的在校学生，最多可以参加2次全球总决赛和4次区域选拔赛。<br />
　　比赛期间，每队使用1台电脑需要在5个小时内使用C、C++、Pascal或Java中的一种编写程序解决8或10个问题（通常是区域选拔赛8题，全球总决赛10题）。程序完成之后提交裁判运行，运行的结果会判定为正确或错误两种并及时通知参赛队。而且有趣的是每队在正确完成一题后，组织者将在其位置上升起一只代表该题颜色的气球。<br />
　　最后的获胜者为正确解答题目最多且总用时最少的队伍。每道试题用时将从竞赛开始到试题解答被判定为正确为止，其间每一次提交运行结果被判错误的话将被加罚20分钟时间，未正确解答的试题不记时。例如：A、B两队都正确完成两道题目，其中A队提交这两题的时间分别是比赛开始后1:00和2:45，B队为1:20和2:10，但B队有一题提交了2次。这样A队的总用时为1:00+2:45=3:45而B队为1:20+2:10+0:20=3:50，所以A队以总用时少而获胜。<br />
　　与其它计算机程序竞赛（例如国际信息学奥林匹克，IOI）相比，ACM/ICPC的特点在于其题量大，每队需要5小时内完成8道题目，甚至更多。另外一支队伍3名队员却只有1台电脑，使得时间显得更为紧张。因此除了扎实的专业水平，良好的团队协作和心理素质同样是获胜的关键。此种大赛对参赛学生的逻辑分析能力、策略制定和脑力方面具有极大的挑战性。大赛提倡在压力较大的情况下，培养学生的创造力、团队合作精神以解决竞赛的问题，从而挑选和发掘世界上最优秀的程序设计人才。<br />
　　也正因为如此，随着ACM比赛的日益发展壮大，原来越多的企业体会到：经过ACM训练的选手和没经过训练的选手之间存在的巨大差距，因此，企业对于参加过ACM训练的选手十分青睐，工作十分好找。IBM中国有限公司大连分公司总经理张伟第36届大连区域赛时说：我们赞助ICPC，一是我们的企业责任，二是我们希望通过这一赛事找到我们所需要的尖端人才。</p>
						</blockquote>
