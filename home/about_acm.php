					<h2 class="title">湖南省ACM比赛情况</h2>
					<p class="byline"></p>
					<div class="entry">
						<blockquote>
<p>　　湖南省从2005年开始举办省赛，一共举办了七届，大赛由湖南省教育厅主办、湖南省高教学会计算机基础教育专业委员会协办，省内高校承办。 <br />
　　HNCPC（Hunan Collegiate Programming Contest, 简称HNCPC）竞赛采用ACM/ICPC规则与方式，其不仅强调大学生对科学基础知识与计算机本科专业课程知识的掌握，更突出大学生全面素质和能力的培养。参赛3名队员共用一台电脑，需要队员拥有良好的团队协作精神。HNCPC是采用5个小时全封闭的竞赛模式，参赛队员与外界完全隔离，完全独立完成竞赛，是参赛队员实际能力的真实表露，且HNCPC采用严谨、客观的评判规则（如严密的测评系统PC2与严格的测试数据），排除了因评委的主观因素而造成的评审不公平的现象，其成绩可信度甚高，因此，社会各界对HNCPC成绩认可度很高。<br />
　　随着赛事的普及，湖南各高校对ACM也日益重视。今年9月15日，&ldquo;蓝狐杯&rdquo;第七届湖南省大学生程序设计大赛在湖南农业大学举行，本届有来自全省各地的36所高校、省内5所知名中学共138个代表队、400多名学生参赛。<br />
　　在我省参赛的高校中，目前以国防科大获得的一等奖数最多。</p>
						</blockquote>
